public class Piece
{
    private int piece;

    public Piece(int value)
    {
        piece = value;
    } // end constructor

    public int getPieceValue()
    {
        return piece;
    } // end int getPieceValue

    public void setPlaced(int value)
    {
        piece = value;
    } // end void setPlaced
} // end class Piece
